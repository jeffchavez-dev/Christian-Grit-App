export function getDailyVerse(date) {
  const start = new Date(date.getFullYear(), 0, 0);
  const day = Math.floor((date - start) / 86400000);
  return DAILY_VERSES[day % DAILY_VERSES.length];
}

export function getDailyCatechism(date) {
  const start = new Date(date.getFullYear(), 0, 0);
  const day = Math.floor((date - start) / 86400000);
  return CATECHISM[(day - 1) % CATECHISM.length];
}

export function getMcCheyneForDate(date) {
  const start = new Date(date.getFullYear(), 0, 1);
  const day = Math.floor((date - start) / 86400000) + 1;
  return MCHEYNE[(day - 1) % MCHEYNE.length];
}
